class PopupFormularConfiguration {
  constructor() {}

  setDocName(docName) {
    this.docName = docName;
    return this;
  }

  setInputGroups(inputGroups) {
    this.inputGroups = inputGroups;
    return this;
  }

  setInputs(inputs) {
    this.inputs = inputs;
    return this;
  }
}

export default PopupFormularConfiguration;
